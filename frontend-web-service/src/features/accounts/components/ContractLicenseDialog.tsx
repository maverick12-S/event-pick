const ContractLicenseDialog: React.FC<ContractLicenseDialogProps> = ({ open, onClose, onAccept }) => {
  const scrollRef = useRef<HTMLDivElement | null>(null);
  const [hasReachedEnd, setHasReachedEnd] = useState(false);

  useEffect(() => {
    if (!open) {
      setHasReachedEnd(false);
    }
  }, [open]);

  const lines = useMemo(() => LICENSE_TEXT.split('\n'), []);

  const handleScroll = () => {
    const element = scrollRef.current;
    if (!element) return;
    const threshold = 24;
    const reached = element.scrollTop + element.clientHeight >= element.scrollHeight - threshold;
    if (reached) {
      setHasReachedEnd(true);
    }
  };

  return (
    <Dialog
      open={open}
      onClose={onClose}
      fullWidth
      maxWidth="md"
      PaperProps={{
        sx: {
          borderRadius: '18px',
          background: 'linear-gradient(180deg, rgba(246,250,255,0.98), rgba(233,242,255,0.98))',
        },
      }}
    >
      <DialogTitle sx={{ color: '#12325c', fontWeight: 800, pb: 1 }}>利用ライセンス承諾書</DialogTitle>
      <DialogContent sx={{ pt: 0.5 }}>
        <Typography sx={{ color: '#355072', fontSize: '0.9rem', mb: 1.5 }}>
          本文を最後までスクロールすると「承諾してダウンロード」ボタンが有効になります。
        </Typography>
        <Box
          ref={scrollRef}
          onScroll={handleScroll}
          sx={{
            maxHeight: 440,
            overflowY: 'auto',
            px: 1.5,
            py: 1.4,
            borderRadius: '14px',
            border: '1px solid rgba(36, 74, 122, 0.18)',
            background: 'rgba(255,255,255,0.88)',
          }}
        >
          {lines.map((line, index) => (
            <Typography
              key={`${index}-${line.slice(0, 8)}`}
              sx={{
                whiteSpace: 'pre-wrap',
                color: '#18385f',
                fontSize: '0.92rem',
                lineHeight: 1.8,
                fontWeight: line.startsWith('第') ? 700 : 500,
                mb: line === '' ? 1.2 : 0,
              }}
            >
              {line || ' '}
            </Typography>
          ))}
        </Box>
      </DialogContent>
      <DialogActions sx={{ px: 3, pb: 2.4, pt: 1.2, gap: 1 }}>
        <Button onClick={onClose} sx={{ fontWeight: 700, color: '#355072' }}>キャンセル</Button>
        <Button
          variant="contained"
          onClick={onAccept}
          disabled={!hasReachedEnd}
          sx={{
            fontWeight: 800,
            background: 'linear-gradient(135deg, #2d91ff, #1666c7)',
            boxShadow: '0 10px 22px rgba(22, 102, 199, 0.24)',
          }}
        >
          承諾してダウンロード
        </Button>
      </DialogActions>
    </Dialog>
  );
};

export default ContractLicenseDialog;